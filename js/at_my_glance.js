$(document).ready(function() {
        
     $("#height .features").click(function() 
    {
        
        
        if ($('#height div').is(':hidden'))
        { 
            $("#height .options").show();
        }
        else
        {
          $("#height .options").hide();
        }
       
    });
    
    $("#height .value").click(function() 
    {
        
        
        if ($('#height div').is(':hidden'))
        { 
            $("#height .options").show();
        }
        else
        {
          $("#height .options").hide();
        }
        
    });
    
   
   
   
    
});